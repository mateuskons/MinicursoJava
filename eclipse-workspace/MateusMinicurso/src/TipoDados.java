
public class TipoDados {

	public static void main(String[] args) {
		// Declaração de variáveis
		byte numeroByte =121; 
		short inteiroCurto =32300;
		int inteiro = 1234444;
		long inteiroLongo = 123333L;
		
		// Números reais
		float realFloat =12f;
		double realDuplo =45;
		char caracter = 'l';
		boolean valorBoleano = true;
		String palavra = "Palavra grande";
		
		System.out.println(" Byte: "+numeroByte);
		System.out.println(" Short: "+inteiroCurto);
		System.out.println(" Inteiro: "+inteiro);
		System.out.println(" Long: "+inteiroLongo);
		System.out.println(" float: "+realFloat);
		System.out.println(" Double: "+realDuplo);
		System.out.println(" Char: "+caracter);
		System.out.println(" Boolean: "+valorBoleano);
		System.out.println(" String: "+palavra);
		System.out.println(" String: "+palavra);

	}

}
